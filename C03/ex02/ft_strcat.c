#include <unistd.h>

char	*ft_strcat(char *dest, char *src)
{
	int n;

	n = 0;
	while (dest[n])
		n++;
	while (*src)
	{
		dest[n++] = *src;
		src++;
	}
	dest[n] = '\0';
	return (dest);
}
