#include "ft.h"

int	main(int ac, char **av)
{
	int	n;

	if (ac == 1)
	{
		ft_readstdin();
		return (0);
	}
	n = 0;
	while (++n < ac)
		ft_readfile(av[n]);
	return (0);
}
