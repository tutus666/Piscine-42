int	ft_sqrt(int nb)
{
	int sqrt;

	if (nb <= 0)
		return (0);
	sqrt = 2;
	if (nb % 2)
		sqrt = 1;
	while (sqrt <= (nb / sqrt))
	{
		if (sqrt * sqrt == nb)
			return (sqrt);
		sqrt += 2;
	}
	return (0);
}
