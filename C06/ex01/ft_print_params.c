#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_putstr(char *str)
{
	while (*str)
		ft_putchar(*str++);
	ft_putchar('\n');
}

int		main(int ac, char **av)
{
	int n;

	n = 0;
	while (n++ < ac - 1)
		ft_putstr(av[n]);
	return (0);
}
