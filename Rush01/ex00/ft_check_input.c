int		ft_check_input(char *str)
{
	int n;

	n = 0;
	while (str[n])
	{
		if (!(n % 2) && (str[n] < '1' || str[n] > '4'))
			return (0);
		else if (n % 2 && str[n] != ' ')
			return (0);
		n++;
	}
	return (n == 31 ? 1 : 0);
}
